#include "../minishell.h"

int	ft_env(char **env, int fd)//ok env
{
	int	i;

	i = -1;
	while (env[++i])
		if (find_eq_sign(env[i]) != -1)
			ft_putendl_fd(env[i], fd);
	return (1);
}
